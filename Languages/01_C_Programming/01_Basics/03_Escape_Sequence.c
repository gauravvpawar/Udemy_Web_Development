#include<stdio.h>
int main()
{
    printf("Hello,\nWorld!\n"); // Newline
    printf("Name:\tJohn Doe\n"); // Horizontal tab
    printf("Path: C:\\Users\\Public\n"); // Backslash
    printf("She said, \"Hello!\"\n"); // Double quote
    printf("Are you there \r");//Carriage return - moves the cursor to the beginning of the current line.
    return 0;
}